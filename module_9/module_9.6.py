'''Задача:
Напишите функцию-генератор all_variants(text), которая принимает строку text и возвращает объект-генератор, при каждой итерации которого будет возвращаться подпоследовательности переданной строки.

Пункты задачи:
Напишите функцию-генератор all_variants(text).
Опишите логику работы внутри функции all_variants.
Вызовите функцию all_variants и выполните итерации.'''

def all_variants(text):
    n = len(text)
    # Итерируемся по всем возможным начальным индексам
    for start in range(n):
        # Итерируемся по всем возможным конечным индексам
        for end in range(start + 1, n + 1):
            # Возвращаем срез строки от start до end
            yield text[start:end]

# Пример использования функции
a = all_variants("abc")
for i in a:
    print(i)
